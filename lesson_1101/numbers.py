"""Тип данных Числа. int, float"""

n1 = 5
n2 = 7
n3 = 0.75
n4 = 1.56

print(n1 + n2)  # плюс
print(n1 - n2)  # минус
print(n1 * n2)  # умножить
print(n1 / n2)  # делить

print(n1 ** 2)  # n1 возвести в степень 2
print(n1 % n2)  # вернуть остаток от деления n1 на n2
print(n1 // n2)  # разделить целочисленно (без остатка) n1 на n2

